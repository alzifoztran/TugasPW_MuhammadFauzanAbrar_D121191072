function query($query)
{
  $conn = koneksi();

  $result = mysqli_query($conn, $query);

  // jika hasilnya hanya 1 data
  if (mysqli_num_rows($result) == 1) {
    return mysqli_fetch_assoc($result);
  }

  $rows = [];
  while ($row = mysqli_fetch_assoc($result)) {
    $rows[] = $row;
  }

  return $rows;
}

function upload()
{
  $nama_file = $_FILES['gambar']['name'];
  $tipe_file = $_FILES['gambar']['type'];
  $error = $_FILES['gambar']['error'];
  $tmp_file = $_FILES['gambar']['tmp_name'];
  $size = $_FILES['gambar']['size'];

  // cek apakah pilih file atau tidak
  if ($error == 4) {
    return "nophoto.jpg";
  }

  //cek ekstensi file jpg, jpeg, png
  $ekstensi_file = explode('.', $nama_file);
  $ekstensi_file = strtolower(end($ekstensi_file));
  $daftar_ekstensi = ['jpg', 'jpeg', 'png'];
  if (!in_array($ekstensi_file, $daftar_ekstensi)) {
    echo "<script>
    alert('file harus berekstensi jpg, jpeg, atau png !!!')
    </script>";
    return false;
  }

  //cek tipe file
  if ($tipe_file != "image/jpeg" && $tipe_file != "image/png") {
    echo "<script>
    alert('file harus berupa gambar !!!')
    </script>";
    return false;
  }

  //generate nama baru
  $nama_file_baru = uniqid() . '.' . $ekstensi_file;

  move_uploaded_file($tmp_file, "img/" . $nama_file_baru);

  return $nama_file_baru;
}

function tambah($data)
{
  $conn = koneksi();

  $nama = htmlspecialchars($data['nama']);
  $nrp = htmlspecialchars($data['nrp']);
  $email = htmlspecialchars($data['email']);
  $jurusan = htmlspecialchars($data['jurusan']);
  // $gambar = htmlspecialchars($data['gambar']);

  $gambar = upload();
  if (!$gambar) {
    return false;
  }

  $query = "INSERT INTO
              mahasiswa
            VALUES
            (null, '$nama', '$nrp', '$email', '$jurusan', '$gambar');
          ";
  mysqli_query($conn, $query) or die(mysqli_error($conn));
  return mysqli_affected_rows($conn);
}

function hapus($id)
{
